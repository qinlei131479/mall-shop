package com.tigshop.adminapi.controller.finance;

import com.tigshop.bean.dto.finance.AccountPanelDTO;
import com.tigshop.bean.vo.finance.AccountPanelVO;
import com.tigshop.service.finance.AccountPanelService;
import io.swagger.v3.oas.annotations.Operation;
import io.swagger.v3.oas.annotations.tags.Tag;
import jakarta.annotation.Resource;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

/**
 * 账户总览控制器
 *
 * @author Tigshop团队
 * @create 2024年12月04日 09:30
 */
@RestController
@RequestMapping("/adminapi/finance/accountPanel")
@Tag(name = "账户总览", description = "账户总览功能")
public class AccountPanelController {
    @Resource
    private AccountPanelService accountPanelService;

    @GetMapping("/list")
    @Operation(summary = "获取数据")
    public AccountPanelVO list(AccountPanelDTO listDTO) {
        return accountPanelService.list(listDTO);
    }

}
